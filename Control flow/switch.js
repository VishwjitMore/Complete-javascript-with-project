// so basically we have studied conditionals that is use of if else if and else 
//  now we will study use of switch cases
let month=3;
switch (month) {
    case 1:
        console.log("jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;
    default:
        console.log("default value")
        break;
}